void hex_encode(const uint8_t *in, char *out, size_t in_bytes)
{
	for (size_t i = 0; i < in_bytes; ++i) {
		out[2*i] = hex_chars[in[i] >> 4];
		out[2*i + 1] = hex_chars[in[i] & 0xF];
	}
}

void hex_decode(const char *in, uint8_t *out, size_t in_bytes)
{
	for (size_t i = 0; i < in_bytes; i+=2) {
		out[i>>1] = (char_to_hex[(size_t)in[i]] << 4) | char_to_hex[(size_t)in[i+1]];
	}
}
